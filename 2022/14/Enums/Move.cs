namespace _14.Enums;

/// <summary>
/// Defines the possible directions a grain of sand can move.
/// </summary>
internal enum Move
{
    /// <summary>
    /// Down and left.
    /// </summary>
    BottomLeft = -1,

    /// <summary>
    /// Straight down.
    /// </summary>
    Bottom = 0,

    /// <summary>
    /// Down and right.
    /// </summary>
    BottomRight = 1
}